    <p>The Microdata Library includes datasets that have been produced by the World Bank, such as impact evaluation of the Bank’s operations or research on development issues. The Microdata Library also includes datasets from other international organizations, statistical agencies and other agencies in low and middle-income countries. </p>
<p>The microdata in the Microdata Library have been collected through sample surveys of households, business establishments or other facilities. Datasets may also originate from population, housing or agricultural censuses or through an administrative data collection processes.</p>
<p>The Microdata Library is comprised of a number of <a href="http://microdata.worldbank.org/index.php/contributing-catalogs">Collections</a>. These collections can be browsed and searched individually or centrally from the <a href="http://microdata.worldbank.org/index.php/catalog/central">Central Microdata Catalog</a>.  The Central Microdata Catalog operates as a portal for all microdata held in the Microdata Library.</p>
<p>All data and surveys within the Microdata Library are documented in compliance with international standards and practices.</p>
<p>Our aim is to provide access to as much information as possible, free of charge, to all users. In some situations, however, especially where the datasets have been provided to the World Bank by other agencies, the ownership of the data remains with that agency and there may be some limitations on how they can be accessed and used. More information on this can be found in our Microdata Library <a href="http://microdata.worldbank.org/index.php/terms-of-use">Terms of Use</a>.</p>
